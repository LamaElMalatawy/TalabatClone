    private void logInBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_logInBtnActionPerformed

        //Get data from textfields of the frame
        String Username = usernameTextField.getText();
        String Password = String.valueOf(PasswordTextField.getPassword());

        PersonDAO p = new PersonDAO();

        //function to check that data entered matches an entery in the database
        p.LogIn(Username, Password);

        if (p.LogIn(Username, Password) == true) {
            char type = p.getPersonType(Username);
            JOptionPane.showMessageDialog(null, "Welcome!");
            //if getPersonType returned 'C', then open Restaurants frame for customers
            if (type == 'C') {
                dispose();
                Restaurants rf = new Restaurants(Username);
                rf.setVisible(true);
                rf.pack();
                rf.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
            } //if getPersonType returned 'O', then open OwnersOptions frame for owners
            else if (type == 'O') {
                dispose();
                OwnerOptions rf = new OwnerOptions(Username);
                rf.setVisible(true);
                rf.pack();
                rf.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
            }
        } //if username or password is missig show an error nessage 
        else if (usernameTextField.getText().equals("") || PasswordTextField.getText().equals("")) {

            JOptionPane.showMessageDialog(null, "Please enter your complete data first then press login");
        } //if data entered doesn't match any entery in the database, display an error message
        else {
            JOptionPane.showMessageDialog(null, "Wrong Entry! Please make sure you entered the right data and try again");
        }

    }//GEN-LAST:event_logInBtnActionPerformed

